use crate::video::draw_text::FontSize;

pub fn get_char_bitmap(c: char, size: FontSize) -> Option<Box<[u8]>> {
    match size {
        FontSize::Normal => get_normal_char_bitmap(c).map(Box::from),
        FontSize::Small => get_small_char_bitmap(c).map(Box::from),
    }
}

